package com.mattvoget.sarlacc.models;

import org.springframework.data.annotation.Id;

public class App {

	@Id
	private String id;
	private String name;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
